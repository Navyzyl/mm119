day_1 = int(input('请输入数字1-7(输入0结束）：'))
if day_1==0:
    print('程序结束！')
elif day_1==1:
    print('MON')
elif day_1==2:
    print('TUE')
elif day_1==3:
    print('WED')
elif day_1==4:
    print('THU')
elif day_1==5:
    print('FRI')
elif day_1==6:
    print('SAT')
elif day_1==7:
    print('SUN')
else:
    print('请重新输入')
